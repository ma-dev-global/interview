import { Injectable } from '@angular/core';
import { HttpClient } from '@angular/common/http';
import { Comment } from './comment.model'

@Injectable({
  providedIn: 'root'
})
export class CommentService {

  private comments: Comment[] = []

  constructor(private httpClient: HttpClient) {
    this.comments = [];
  }

  public async get(): Promise<Comment[]> {
    let result = await this.httpClient.get<Comment[]>('/api/comments').toPromise();
    return result;
  }

  public async post(comment): Promise<void> {
    let result = await this.httpClient.post('/api/comments', comment).toPromise();
    return;
  }
}

















